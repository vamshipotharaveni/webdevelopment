public class SSwitch{
	public static void main(String args[])
	{
	String a="Ajay";
	switch(a){
	
	case "vamshi":
	System.out.println("My name is vamshi");
	break;

	case "rajkumar":
	System.out.println("My name is rajkumar");
	break;

 	case "Ajay":
	System.out.println("My name is Ajn");
	break;

	case "ravi":
	System.out.println("My name is ravi");
	break;

	case "teja":
	System.out.println("My name is teja");
	break;
	
	default:
	System.out.println("default name");
}
}
}