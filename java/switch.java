public class switch {
  public static void main(String args[])
	{
	   int a;
		Scanner sc=new Scanner(System.in);
		a=sc.nextInt();
		switch(a)
  		{

			case 2:
			System.out.println("vidu ajay");
			break;

			case 3:
			System.out.println("vidu vamshi");
			break;

			case 4:
			System.out.println("vidu raki");
			break;

			case 5:
			System.out.println("vidu chinna");
			break;
			
			default:
			System.out.println("there is no name");
		}
	}
}


